#ifndef LIBBITCOIN_PROTOCOL_WEB_PROTOCOL_STATUS_HPP
#define LIBBITCOIN_PROTOCOL_WEB_PROTOCOL_STATUS_HPP

#include <cstdint>

namespace libbitcoin {
namespace protocol {
namespace http {

enum class protocol_status : uint16_t
{
    switching = 101,
    ok = 200,
    created = 201,
    accepted = 202,
    no_content = 204,
    multiple_choices = 300,
    moved_permanently = 301,
    moved_temporarily = 302,
    not_modified = 304,
    bad_request = 400,
    unauthorized = 401,
    forbidden = 403,
    not_found = 404,
    internal_server_error = 500,
    not_implemented = 501,
    bad_gateway = 502,
    service_unavailable = 503
};

} // namespace http
} // namespace protocol
} // namespace libbitcoin

#endif
